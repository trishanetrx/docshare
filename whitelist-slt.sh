124.43.0.0/16 203.94.64.0/18 203.115.0.0/18 220.247.192.0/18 222.165.128.0/18"

JAIL=/etc/fail2ban/jail.local

echo "==> Updating fail2ban whitelist with SLT IP ranges..."

# Remove any existing ignoreip line and add fresh one under [DEFAULT]
if grep -q '^ignoreip' "$JAIL"; then
    sed -i "s|^ignoreip.*|ignoreip = 127.0.0.1/8 ::1 ${SLT_RANGES}|" "$JAIL"
else
    sed -i "/^\[DEFAULT\]/a ignoreip = 127.0.0.1/8 ::1 ${SLT_RANGES}" "$JAIL"
fi

echo "==> Restarting fail2ban..."
systemctl restart fail2ban
sleep 2

echo "==> Done. Verifying..."
fail2ban-client status | grep "Jail list"
echo ""
echo "ignoreip line (truncated):"
grep ignoreip "$JAIL" | cut -c1-100
